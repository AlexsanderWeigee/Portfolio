function criarCertificado(certificado) {
    return `
    <div class="cards">
        <div class="cards__card">
            <div class="cards__imgTecnologia">
                <img class="Img" src="${certificado.imagem}" alt="Imagem do certificado">
            </div>
        </div>

        <div class="cards__separacao">
            <div class="cards__Textos">
                <h1 class="cards__Titulo">${certificado.titulo}</h1>

                <p class="card__Texto">${certificado.descricao}</p>

                <small class="card__data">
                    ${new Date(certificado.date + "T00:00:00").toLocaleDateString("pt-BR")}
                </small>
                <div class="card__btn">
                    <div class="btn">
                        <a href="${certificado.link}" class="explorar" target="_blank">Ver certificado</a>
                    </div>
                </div>
            </div>
        </div>
    </div>
    `;
}

certificados.sort((a, b) => {
    return new Date(b.date) - new Date(a.date);
});

document.addEventListener("DOMContentLoaded", () => {
    const container = document.getElementById("certificados-lista");

    if (!container) return;

    certificados.forEach(certificado => {
        container.insertAdjacentHTML("beforeend", criarCertificado(certificado));
    });
});
